package review;

import java.util.Arrays;

public class BoxingUnboxing {

	public static void main(String[] args) {
		//boolean
		System.out.println("boolean: " + String.valueOf(true));
		//charasfdsdf
		System.out.println("char: " + String.valueOf('a'));
		//int
		System.out.println("int: " + String.valueOf(10));
		//long
		System.out.println("long: " + String.valueOf(1000));
		//float
		System.out.println("float: " + String.valueOf(3.14f));
		//double
		System.out.println("double: " + String.valueOf(3.1412));

	}

}
